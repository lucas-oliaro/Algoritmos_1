package Guia_3.Parte_8_Integradores.Ejercicio_85;

public class Bloque {
    private int largo; 
    private int ancho;
    private int altura;
    
    public Bloque(int largo, int ancho, int altura) {
        this.largo = largo;
        this.ancho = ancho;
        this.altura = altura;
    }

    // Getters
    public int getLargo() {
        return largo;
    }
    public int getAncho(){
        return ancho;
    }
    public int getAltura(){
        return altura;
    }
    
}
