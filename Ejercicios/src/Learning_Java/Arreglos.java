package Learning_Java;

public class Arreglos {
    public static void main(String[] args){
        int  [] numeros = new int[3];

        numeros[0] = 1;
        numeros[1] = 2;
        numeros[2] = 3;
        
        for (int i=0; i<numeros.length; i++){
            System.out.println(numeros[i]);
        }
    }
    
}
